import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import process from "node:process";
import { fileURLToPath } from "node:url";
import { Eta } from "eta";
import { parse as parseJsonc, printParseErrorCode } from "jsonc-parser";

const CHECK = process.argv.includes("--check");
const CI = process.env.CI === "true";
const SOURCE = "src/main/resources/feature-flags/feature-flags.jsonc";
const JAVA = "src/main/java/com/researchspace/featureflags/FeatureFlags.java";
const TS = "src/main/webapp/ui/src/featureFlags/generatedFeatureFlags.ts";
const MAX_NAME_LENGTH = 128;

const eta = new Eta({
  views: path.join(path.dirname(fileURLToPath(import.meta.url)), "templates"),
  autoEscape: false,
  autoTrim: false,
});

const parseManifest = (text) => {
  const errors = [];
  const manifest = parseJsonc(text, errors, { allowTrailingComma: true });
  if (errors.length > 0) {
    const [{ error, offset }] = errors;
    throw new Error(`${SOURCE} is invalid JSONC at offset ${offset}: ${printParseErrorCode(error)}`);
  }
  return manifest;
};

const upperSnake = (name) => name.replace(/([a-z0-9])([A-Z])/g, "$1_$2").toUpperCase();

const parseExpiry = (flagName, value) => {
  const date = new Date(`${value}T00:00:00Z`);
  if (Number.isNaN(date.getTime()) || date.toISOString().slice(0, 10) !== value) {
    throw new Error(`Feature flag ${flagName} expires must be a valid ISO date`);
  }
  return date;
};

const readManifest = async () => {
  const text = await readFile(SOURCE, "utf8");
  const manifest = parseManifest(text);
  const flags = manifest.flags;
  if (!Array.isArray(flags)) throw new Error("feature-flags.jsonc must contain a flags array");
  const seen = new Set();
  for (const flag of flags) {
    if (!flag || typeof flag !== "object") throw new Error("Each flag must be an object");
    if (
      typeof flag.name !== "string" ||
      flag.name.length > MAX_NAME_LENGTH ||
      !/^[a-z][a-zA-Z0-9]*$/.test(flag.name)
    ) {
      throw new Error(`Invalid feature flag name: ${flag.name}`);
    }
    if (seen.has(flag.name)) throw new Error(`Duplicate feature flag name: ${flag.name}`);
    seen.add(flag.name);
    if (flag.default !== undefined && typeof flag.default !== "boolean") {
      throw new Error(`Feature flag ${flag.name} default must be boolean`);
    }
    for (const required of ["description", "owner"]) {
      if (typeof flag[required] !== "string" || flag[required].trim() === "") {
        throw new Error(`Feature flag ${flag.name} must define ${required}`);
      }
    }
    if (typeof flag.expires !== "string") {
      throw new Error(`Feature flag ${flag.name} must define expires`);
    }
    const expiry = parseExpiry(flag.name, flag.expires);
    const today = new Date(new Date().toISOString().slice(0, 10));
    if (CI && expiry < today) {
      throw new Error(`Feature flag ${flag.name} expires has passed`);
    }
  }
  return flags;
};

const generatedBanner = (comment) => `${comment} Generated by scripts/generate-feature-flags.mjs. Do not edit.`;

const renderJava = (flags) =>
  eta.render("./FeatureFlags.java.eta", {
    banner: generatedBanner("//"),
    flags: flags.map((flag) => ({ name: flag.name, constant: upperSnake(flag.name) })),
  });

const renderTypescript = (flags) =>
  eta.render("./generatedFeatureFlags.ts.eta", {
    banner: generatedBanner("//"),
    flags,
  });

const assertCurrentOrWrite = async (file, content) => {
  if (CHECK) {
    const current = await readFile(file, "utf8");
    if (current !== content) {
      throw new Error(`${file} is out of date. Run pnpm run feature-flags:generate.`);
    }
    return;
  }
  await mkdir(path.dirname(file), { recursive: true });
  await writeFile(file, content);
};

const flags = await readManifest();
await assertCurrentOrWrite(JAVA, renderJava(flags));
await assertCurrentOrWrite(TS, renderTypescript(flags));
